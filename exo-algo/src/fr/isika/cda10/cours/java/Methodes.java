package fr.isika.cda10.cours.java;

public class Methodes {
	
	/*
	 * Méthodes (ou fonction)
	 * 
	 * - Nom donné aux opérations
	 * - Méthodes associées aux classes (pas de méthode sans classe)
	 * - Surcharge (en anglais, overloading) des méthodes possible 
	 * (signifie redefinir une méthode dans une autre classe pour faire quelque 
	 * chose de différent mais on  garde le nom, le type de retour et la visibilité, voir POO)
	 * - Méthode d'instance
	 * - Méthode de classe avec mot réservé static 
	 * (signifie : cette méthode peut être utilisé sans obtenir d'instance de la classe, static 
	 * signifie qu'elle n'existe qu'en un seul exemplair)
	 * - Prototype de la méthode : public static void main(String args[])
	 * - Passage d'arguments (appelé aussi paramètres) se fait :
	 *   - Par valeur pour les types pimitifs
	 *   - Par référence pour les objets et les tableaux
	 *   
	 *   
	 *   Comment déclarer une fonction (ses différents composants) :
	 *   
	 *   Visibilité -- Static/Final -- Type de retour ou Void (pas de retour) -- Nom (Arguments ou Paramètres)
	 *   
	 *   params => variables
	 *   
	 */

}
